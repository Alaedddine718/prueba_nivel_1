class maestre():
    def __init__(self,nombre,rango,pergaminosPrestados):
        self.nombre= nombre
        self.rango=rango
        self.pergaminosPrestados=pergaminosPrestados
    def mostrar_nombre (self):
         return self.nombre 
    def mostrar_rango (self):
        return self.rango
    def mostrar_pergaminosPrestados(self):
        return self.pergaminosPrestados  
        


